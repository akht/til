package atcoder.ABC115

fun main(args: Array<String>) {
    val D = readLine()!!.toInt()

    if (D == 25) {
        println("Christmas")
    } else if (D == 24) {
        println("Christmas Eve")
    } else if (D == 23) {
        println("Christmas Eve Eve")
    } else if (D == 22) {
        println("Christmas Eve Eve Eve")
    }
}